package com.i3c.receptorioio;

public class Movimiento {
	private int veloc;
	private int giro;
	private long time;
	public int getVeloc() {
		return veloc;
	}
	public void setVeloc(int veloc) {
		this.veloc = veloc;
	}
	public int getGiro() {
		return giro;
	}
	public void setGiro(int giro) {
		this.giro = giro;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
}
